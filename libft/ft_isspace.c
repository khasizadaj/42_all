int	ft_isspace(int c)
{
	if ((char) c != '\f' && (char) c != '\n' && (char) c != '\r'
		&& (char) c != '\t' && (char) c != '\v' && (char) c != 32 
		&& (char) c != ' ')
		return (0);
	return (1);
}