int	validate_input(char *str)
{	
	int	i;

	i = 0;
	while (str[i])
	{
		if (!(('0' <= str[i] && str[i] <= '9') || str[i] == ' '))
			return (0);
		if ((i % 2) == 0)
		{
			if (!(('0' <= str[i]) && (str[i] <= '9')))
				return (0);
		}
		i++;
	}
	return (1);
}
